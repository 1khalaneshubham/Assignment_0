// Q.2 Write a java program , take a number and print whether it is less than 10 or greater than 10.
// INPUT: var1 = 5
// OUTPUT: 5 is less than 10;


class Demo{
	public static void main(String[] args){

		//int var = 5;  input 1
		//int var = 16; input 2
		int var = 10;

		if( var == 10){
			System.out.println("??");
		}
		else if(var > 10){
			System.out.println(var+" is less than 10");
		}
		else{
			System.out.println(var+" is greater than 10");
		}
	}
}
